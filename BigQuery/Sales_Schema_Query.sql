 -- create dataset 
CREATE SCHEMA IF NOT EXISTS `Sales_Dataset`
OPTIONS (
   default_table_expiration_days = 60,
   location = 'US'
);


 -- create users table 
CREATE TABLE IF NOT EXISTS `Sales_Dataset.Users`
(
   id INT64 NOT NULL , -- you marked it as rquired 
   date_creation DATETIME ,
   name STRING, 
   email STRING,
   address STRING , 
   city STRING , 
   state STRING, 
   zip INT64, 
   birth_date DATE ,
   password STRING , 
   source_channel STRING,
   PRIMARY KEY (id) NOT ENFORCED 
);


 -- create products table 
CREATE TABLE IF NOT EXISTS `Sales_Dataset.Products`
(
  product_id INT64 NOT NULL ,
  prduct_added_date DATETIME, 
  category STRING , 
  price NUMERIC NOT NULL ,
  title STRING,
  vendor STRING,
  PRIMARY KEY (product_id) NOT ENFORCED 
); 

 -- create orders table 
CREATE TABLE IF NOT EXISTS `Sales_Dataset.Orders`
(
 order_id INT64 NOT NULL ,
 order_date DATETIME,
 user_id INT64 NOT NULL,
 product_id INT64 NOT NULL ,
 discount FLOAT64,
 quantity INT64,
 tax NUMERIC,
 PRIMARY KEY (order_id) NOT ENFORCED,
 FOREIGN KEY (user_id) REFERENCES `Sales_Dataset.Users`(id) NOT ENFORCED,
 FOREIGN KEY (product_id) REFERENCES `Sales_Dataset.Products`(product_id) NOT ENFORCED 

)
PARTITION BY DATE_TRUNC(order_date,MONTH)
CLUSTER BY user_id;

