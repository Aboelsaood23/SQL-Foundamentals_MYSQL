create database if not exists sales_database;
use sales_database;
create table Customers (
    customer_id varchar (9) not null primary key ,
    first_name varchar(10),
    last_name varchar(10),
    gender char (1),
    city varchar(15)
);
create table products (
    product_id varchar(8) not null primary key ,
    product_name varchar(30),
    product_price decimal(2,2),
    product_category varchar(25)
);
create table stores (
    store_id varchar(9) not null primary key ,
    city varchar(12),
    state varchar(15),
    location varchar(10)
);
create table sales  (
    order_id varchar(9) not null primary key,
    order_date date,
    customer_id varchar(9),
    product_id varchar(6),
    units_sold tinyint,
    total_amount decimal(4,2),
   store_id varchar(9),
   foreign key (store_id) references stores(store_id),
   foreign key (customer_id) references customers(customer_id),
   foreign key (product_id) references products(product_id)
);
